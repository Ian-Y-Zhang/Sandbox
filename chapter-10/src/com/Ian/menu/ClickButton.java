package com.Ian.menu;

/**
 * 
 * click类型按钮类
 * 
 * @author Ian
 * @date 2016-05-13
 *
 */

public class ClickButton extends Button {

	private String type;

	private String key;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

}
