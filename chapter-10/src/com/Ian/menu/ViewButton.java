package com.Ian.menu;

/**
 * 
 * view类型按钮类
 * 
 * @author Ian
 * @date 2016-05-13
 *
 */

public class ViewButton extends Button {

	private String type;

	private String url;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

}
